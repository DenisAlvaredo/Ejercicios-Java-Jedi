public class Ejercicio14 {
    public static void main(String[] args) {
        String nombre1 = "Ricardo";
        String nombre2 = "Gabriel";
        String nombre3 = "Lucia";

        int edad1 = 25;
        int edad2 = 44;
        int edad3 = 30;

        int promedio = (edad1+ edad2+ edad3) / 3;

        System.out.println("Nombre: " + nombre1 + "\nEdad: " + edad1);
        System.out.println("Nombre: " + nombre2 + "\nEdad: " + edad2);
        System.out.println("Nombre: " + nombre3 + "\nEdad: " + edad3);

        System.out.println("La edad promedio es: " + promedio);
    }
}
